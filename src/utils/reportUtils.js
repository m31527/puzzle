// 根據分數範圍獲取級別
export const getLevel = (score) => {
  if (score >= 0 && score <= 30) return 1;
  if (score >= 31 && score <= 45) return 2;
  if (score >= 46 && score <= 60) return 3;
  if (score >= 61 && score <= 100) return 4;
  return 1; // 默認為1級
};

